    private void SavebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SavebtnMouseClicked
     if (Fcode.getText().isEmpty() || Seats.getText().isEmpty() || Fsour.getSelectedIndex() == -1 || Fdest.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(this, "Missing Information");
    } else {
        Connection con = null;
        PreparedStatement add = null;

        try {
            // Establish the connection to the database
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");
            
            // Prepare the SQL insert statement
            add = con.prepareStatement("Insert into flighttbl VALUES(?,?,?,?,?)");
            add.setString(1, Fcode.getText());
            add.setString(2, Fsour.getSelectedItem().toString());
            add.setString(3, Fdest.getSelectedItem().toString());
            
            // Get the actual date and convert to java.sql.Date
            java.util.Date utilDate = Fdate.getDate(); // Get the date from the component
            if (utilDate != null) {
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime()); // Convert to SQL date
                add.setDate(4, sqlDate);
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid date");
                return; // Exit the method if date is invalid
            }
            
            add.setInt(5, Integer.valueOf(Seats.getText()));

            // Execute the insert statement
            int row = add.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Flight Added");
                DisplayFlights(); // Refresh the flight list
                Clear(); // Clear input fields
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            // Close resources
            try {
                if (add != null) add.close();
                if (con != null) con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_SavebtnMouseClicked

    private void DeletebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeletebtnMouseClicked
       if (key == null) {
        JOptionPane.showMessageDialog(this, "Select a Flight");
    } else {
        Connection con = null;
        PreparedStatement del = null;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");
            String query = "DELETE FROM FlightTbl WHERE Flcode = ?";
            del = con.prepareStatement(query);
            del.setString(1, key); // Use prepared statement to avoid SQL injection
            
            int rowsAffected = del.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Flight Deleted");
                DisplayFlights();
                Clear();
            } else {
                JOptionPane.showMessageDialog(this, "Flight not found or already deleted.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting flight: " + e.getMessage());
        } finally {
            // Close resources in finally block
            try {
                if (del != null) del.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_DeletebtnMouseClicked

