    private void EditbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditbtnMouseClicked
      if (key == null) {
        JOptionPane.showMessageDialog(this, "Select A Flight");
    } else {
        Connection con = null;
        PreparedStatement updateStmt = null;

        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");
            String query = "UPDATE FlightTbl SET FlSource=?, FlDest=?, FlDate=?, FlSeats=? WHERE Flcode=?";
            updateStmt = con.prepareStatement(query);
            
            // Set the parameters for the prepared statement
            updateStmt.setString(1, Fsour.getSelectedItem().toString());
            updateStmt.setString(2, Fdest.getSelectedItem().toString());
            
            // Handle date conversion
            if (Fdate.getDate() != null) {
                java.util.Date utilDate = Fdate.getDate();
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                updateStmt.setDate(3, sqlDate); // Set the SQL date
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid date.");
                return; // Exit if the date is invalid
            }
            
            updateStmt.setInt(4, Integer.valueOf(Seats.getText())); // Ensure it's set as an integer
            updateStmt.setString(5, key); // Set the flight code to identify which record to update

            // Execute the update statement
            int row = updateStmt.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Flight Updated");
                DisplayFlights(); // Refresh the flight list
                Clear(); // Clear input fields
            } else {
                JOptionPane.showMessageDialog(this, "No records updated. Please check your input.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            // Close resources
            try {
                if (updateStmt != null) updateStmt.close();
                if (con != null) con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error closing resources: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_EditbtnMouseClicked

