    private void PeditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PeditMouseClicked
     if (key == 0) {
        JOptionPane.showMessageDialog(this, "Select a Passenger");
    } else {
        Connection con = null;
        PreparedStatement checkStmt = null;
        PreparedStatement updateStmt = null;

        try {
            // Establish the connection to the database
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");

            // Check if the passport number already exists
            String checkQuery = "SELECT COUNT(*) FROM PassengersTbl WHERE PPass = ? AND Pid <> ?";
            checkStmt = con.prepareStatement(checkQuery);
            checkStmt.setString(1, PassNo.getText()); // Check for the entered passport number
            checkStmt.setInt(2, key); // Exclude the current passenger ID
            
            ResultSet rs = checkStmt.executeQuery();
            if (rs.next() && rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "This passport number is already in use by another passenger.");
                return; // Exit if the passport number is a duplicate
            }

            // Proceed to update the passenger details
            String updateQuery = "UPDATE PassengersTbl SET Pname=?, PNat=?, PGen=?, PPass=?, PAdd=?, Pphone=? WHERE Pid=?";
            updateStmt = con.prepareStatement(updateQuery);
            updateStmt.setString(1, Pname.getText());
            updateStmt.setString(2, Nationcb.getSelectedItem().toString());
            updateStmt.setString(3, Gencb.getSelectedItem().toString());
            updateStmt.setString(4, PassNo.getText());
            updateStmt.setString(5, PAdd.getText());
            updateStmt.setString(6, Phone.getText());
            updateStmt.setInt(7, key); // Set the ID of the passenger being updated
            
            // Execute the update
            int rowsAffected = updateStmt.executeUpdate();
            
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Passenger Updated Successfully");
                DisplayPassengers(); // Refresh the passenger list
                Clear(); // Clear input fields
            } else {
                JOptionPane.showMessageDialog(this, "No passenger found with the selected ID.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        } finally {
            // Close resources in the 'finally' block to ensure they are closed even if an exception occurs
            try {
                if (checkStmt != null) checkStmt.close();
                if (updateStmt != null) updateStmt.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error closing connection: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_PeditMouseClicked

