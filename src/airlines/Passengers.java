    private void PSAveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PSAveMouseClicked
    // Check for empty fields
    if (Pname.getText().isEmpty() || PassNo.getText().isEmpty() || PAdd.getText().isEmpty() || Phone.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Missing Information");
        return; // Exit if any field is empty
    }

    try {
        // Establish connection
        Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");

        // Check for duplicate passport numbers
        PreparedStatement checkStmt = Con.prepareStatement("SELECT COUNT(*) FROM passengerstbl WHERE PPass = ?");
        checkStmt.setString(1, PassNo.getText());
        ResultSet rs = checkStmt.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "A passenger with this passport number already exists.");
            return; // Exit if the passport number already exists
        }

        // Check for existing passengers with the same details
        PreparedStatement existingCheckStmt = Con.prepareStatement("SELECT COUNT(*) FROM passengerstbl WHERE Pname = ? AND PPass = ? AND PAdd = ? AND Pphone = ?");
        existingCheckStmt.setString(1, Pname.getText());
        existingCheckStmt.setString(2, PassNo.getText());
        existingCheckStmt.setString(3, PAdd.getText());
        existingCheckStmt.setString(4, Phone.getText());
        ResultSet existingRs = existingCheckStmt.executeQuery();

        if (existingRs.next() && existingRs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "This passenger is already added with the same details.");
            return; // Exit if the passenger with the same details exists
        }

        // Count passengers to assign a new ID
        CountPassengers();

        // Prepare statement to insert new passenger
        PreparedStatement Add = Con.prepareStatement("INSERT INTO passengerstbl VALUES(?,?,?,?,?,?,?)");
        Add.setInt(1, Passid);
        Add.setString(2, Pname.getText());
        Add.setString(3, Nationcb.getSelectedItem().toString());
        Add.setString(4, Gencb.getSelectedItem().toString());
        Add.setString(5, PassNo.getText());
        Add.setString(6, PAdd.getText());
        Add.setString(7, Phone.getText());

        // Execute insertion
        int row = Add.executeUpdate();
        JOptionPane.showMessageDialog(this, "Passenger Added");
        Con.close();
        DisplayPassengers();
        Clear();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }
    }//GEN-LAST:event_PSAveMouseClicked
    
    private void PdeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PdeleteMouseClicked
      if (key == 0) {
        JOptionPane.showMessageDialog(this, "Select a passenger");
    } else {
        try {
            // Establish the connection
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinesdb", "root", "");
            
            // Use a PreparedStatement for safer query execution
            String query = "DELETE FROM PassengersTbl WHERE Pid = ?";
            PreparedStatement delStmt = Con.prepareStatement(query);
            delStmt.setInt(1, key); // Set the passenger ID to delete
            
            // Execute the update
            int rowsAffected = delStmt.executeUpdate();
            
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Passenger Deleted");
                DisplayPassengers(); // Refresh the list
                Clear(); // Clear input fields
            } else {
                JOptionPane.showMessageDialog(this, "No passenger found with the selected ID.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        } finally {
            // Close the connection in the 'finally' block to avoid resource leaks
            try {
                if (Con != null) {
                    Con.close();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error closing connection: " + e.getMessage());
            }
        }
    }
    }//GEN-LAST:event_PdeleteMouseClicked

